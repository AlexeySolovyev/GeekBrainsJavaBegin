public class MathOperations {
    public static void main(String[] args) {
        double a = 5;
        double b = 3;

        System.out.println(a % b);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(b--);
        System.out.println(--b);

        System.out.println(a += b);
        System.out.println(a -= b);
        System.out.println(a *= b);
        System.out.println(a /= b);
        System.out.println(a %= b);
    }
}
